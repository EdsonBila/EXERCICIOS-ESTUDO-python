'''Faça um programa que calcule e mostre a área de um círculo. Sabe-se que: Área = pi * R 2 (este 2 signica elevado ao quadrado) e o valor da variável pi é 3.1415.'''
print('Cálculo da área de um círculo')
raio = float(input('Digite o raio: '))
area = 3.1415 * raio ** 2
print('Área do círculo: ',area)
